const updateChart = _.debounce(async () => {
  let graphTitle = "";
  let graph: any;
  if (appStore.states.graphModeSelect === 0) {
    graph = Object.assign(lv1_subject_data);
    graph.links = lv1_spanning_tree_links.map((item) => {
      return {
        source: item[0],
        target: item[1],
        lineStyle: { width: 3 },
      };
    });
    let count = appStore.states.linksMultiple * 9;
    for (let index in lv1_extend_links) {
      if (Number(index) > count) break;
      // console.log(index);
      graph.links.push({
        source: lv1_extend_links[index][0],
        target: lv1_extend_links[index][1],
        lineStyle: { width: 1 },
      });
    }
  } else if (appStore.states.graphModeSelect === 1) {
    graph = Object.assign(lv2_subject_data);
    graph.links = lv2_spanning_tree_links.map((item) => {
      return {
        source: item[0],
        target: item[1],
        lineStyle: { width: 3 },
      };
    });
    let count = appStore.states.linksMultiple * 146;
    for (let index in lv2_extend_links) {
      if (Number(index) > count) break;
      // console.log(index);
      graph.links.push({
        source: lv2_extend_links[index][0],
        target: lv2_extend_links[index][1],
        lineStyle: { width: 1 },
      });
    }
  }
  let max = Math.max(...graph.nodes.map((item: any) => item.value));
  let min = Math.min(...graph.nodes.map((item: any) => item.value));
  let symbolSize_resize = scaleLinear().domain([min, max]).range([10, 30]);
  graph.nodes = graph.nodes.map((item: any) => {
    item.symbolSize = symbolSize_resize(item.value);
    return item;
  });

  // set graph size

  let option = {
    title: {
      left: "center",
      show: false,
      top: "1%",
      textAlign: "center",
      textStyle: {
        fontSize: 20,
      },
      text: graphTitle,
    },
    tooltip: {},
    legend: {
      show: true,
      left: "auto",
      orient: "vertical",
      data: graph.categories.map(function (a: { name: any }) {
        return a.name;
      }),
    },
    // color: colormap({
    //   colormap: "hsv",
    //   // colormap: "rainbow-soft",
    //   nshades: 19,
    //   format: "rgbaString",
    //   alpha: 0.99,
    // }).reverse(),
    color: appStore.states.colorPicks,
    series: [
      {
        // name: "subject",
        type: "graph",
        layout: "force",
        draggable: "true",
        data: graph.nodes,
        links: graph.links,
        categories: graph.categories,
        roam: true,
        zoom: 1.2,
        label: {
          show: labelShow.value,
          position: "top",
          formatter: "{b}",
        },
        labelLayout: {
          hideOverlap: true,
        },
        force: {
          repulsion: 100,
          layoutAnimation: true,
        },
      },
    ],
  };
  console.log("set opion:", option);
  myChartObjs[0].setOption(option, true);
}, 1000);

